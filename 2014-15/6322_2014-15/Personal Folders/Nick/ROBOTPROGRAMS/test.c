#pragma config(Motor,  mtr_S1_C1_2,     BackRight,     				tmotorNXT, openLoop, reversed)
#pragma config(Motor,  mtr_S1_C2_1,     FrontLeft,     				tmotorNXT, openLoop)
#pragma config(Motor,  mtr_S1_C2_2,     BackLeft,      				tmotorNXT, openLoop)
#pragma config(Motor,  mtr_S1_C3_1,     PullUp,        				tmotorNXT, openLoop)
#pragma config(Servo,  srvo_S1_C4_2,    armlift,              tServoContinuousRotation)
#pragma config(Servo,  srvo_S1_C4_1,    flagturner,           tServoContinuousRotation)
#pragma config(Servo,  srvo_S1_C4_3,    servoright,           tServoStandard)
#pragma config(Servo,  srvo_S1_C4_4,    servoleft,            tServoStandard)
